#[cfg(test)]
mod tests {
    use super::*;
    use crate::generators::Generator;

    #[test]
    fn generate_icons_integration() {
        let root = std::path::Path::new(env!("CARGO_MANIFEST_DIR"))
            .parent()
            .and_then(|p| p.parent());

        let Some(root) = root else { return };
        if !root.join("pnpm-workspace.yaml").exists() {
            return;
        }

        let generator = IconsGenerator;
        let output = generator.generate(root).expect("should generate icons.ts");

        // Header
        assert!(output.contains("AUTO-GENERATED by novanet v0.12.0"));
        assert!(output.contains("SINGLE SOURCE OF TRUTH"));

        // v0.12.0: 58 nodes (39 shared + 19 org)
        assert!(output.contains("NODE ICONS (58 nodes)"));

        // Should have import statement
        assert!(output.contains("import type { NodeType } from '@novanet/core/types'"));

        // Spot check some known nodes with icons
        assert!(output.contains("Project: '🔷'"), "should have Project icon");
        assert!(output.contains("Locale: '🔷'"), "should have Locale icon");
        assert!(output.contains("Entity: '"), "should have Entity entry");
        assert!(
            output.contains("PageGenerated: '"),
            "should have PageGenerated entry"
        ); // v10.9: renamed from PageL10n

        // Helper functions present
        assert!(output.contains("export function getNodeIcon"));
        assert!(output.contains("export const DEFAULT_NODE_ICON"));
    }

    #[test]
    fn render_icons_basic() {
        use crate::parsers::yaml_node::{NodeDef, NodeTrait, ParsedNode};
        use std::path::PathBuf;

        let nodes = vec![
            ParsedNode {
                def: NodeDef {
                    name: "TestNode".to_string(),
                    realm: "org".to_string(),
                    layer: "foundation".to_string(),
                    node_trait: NodeTrait::Defined,
                    knowledge_tier: None,
                    icon: Some("🚀".to_string()),
                    description: "Test".to_string(),
                    standard_properties: None,
                    properties: None,
                    neo4j: None,
                    example: None,
                },
                realm: "org".to_string(),
                layer: "foundation".to_string(),
                source_path: PathBuf::from("test.yaml"),
            },
            ParsedNode {
                def: NodeDef {
                    name: "NoIconNode".to_string(),
                    realm: "shared".to_string(),
                    layer: "config".to_string(),
                    node_trait: NodeTrait::Defined,
                    knowledge_tier: None,
                    icon: None, // No icon - should use default
                    description: "Test".to_string(),
                    standard_properties: None,
                    properties: None,
                    neo4j: None,
                    example: None,
                },
                realm: "shared".to_string(),
                layer: "config".to_string(),
                source_path: PathBuf::from("test2.yaml"),
            },
        ];

        let output = render_icons(&nodes).unwrap();

        // Should have both nodes
        assert!(output.contains("TestNode: '🚀'"));
        assert!(output.contains("NoIconNode: '📄'")); // Default icon

        // Count
        assert!(output.contains("NODE ICONS (2 nodes)"));
    }
}
