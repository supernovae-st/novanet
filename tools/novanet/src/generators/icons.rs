#[cfg(test)]
mod tests {
    use super::*;
    use crate::generators::Generator;
    use serial_test::serial;

    #[test]
    #[serial]
    fn generate_icons_integration() {
        let root = std::path::Path::new(env!("CARGO_MANIFEST_DIR"))
            .parent()
            .and_then(|p| p.parent());

        let Some(root) = root else { return };
        if !root.join("pnpm-workspace.yaml").exists() {
            return;
        }

        let generator = IconsGenerator;
        let output = generator.generate(root).expect("should generate icons.ts");

        // Header
        assert!(output.contains("AUTO-GENERATED by novanet v0.13.0"));
        assert!(output.contains("SINGLE SOURCE OF TRUTH"));

        // v0.12.5: 61 nodes with dual format
        assert!(output.contains("NODE ICONS (61 nodes)"));

        // Should have import statement
        assert!(output.contains("import type { NodeType } from '@novanet/core/types'"));

        // v0.12.5: Dual format icons
        assert!(
            output.contains("Project: { web:"),
            "should have Project with dual format"
        );
        assert!(
            output.contains("Locale: { web:"),
            "should have Locale with dual format"
        );
        assert!(
            output.contains("Entity: { web:"),
            "should have Entity with dual format"
        );
        assert!(
            output.contains("PageNative: { web:"),
            "should have PageNative with dual format (v0.13.0 ADR-029)"
        );

        // v0.12.5: Updated helper functions
        assert!(output.contains("export function getNodeWebIcon"));
        assert!(output.contains("export function getNodeTerminalIcon"));
        assert!(output.contains("export const DEFAULT_NODE_ICON: NodeIcon"));
    }

    #[test]
    fn render_icons_basic() {
        use crate::parsers::yaml_node::{NodeDef, NodeTrait, ParsedNode};
        use std::path::PathBuf;

        let nodes = vec![
            ParsedNode {
                def: NodeDef {
                    name: "TestNode".to_string(),
                    realm: "org".to_string(),
                    layer: "foundation".to_string(),
                    node_trait: NodeTrait::Defined,
                    knowledge_tier: None,
                    icon: Some(NodeIcon {
                        web: "rocket".to_string(),
                        terminal: "🚀".to_string(),
                    }),
                    description: "Test".to_string(),
                    standard_properties: None,
                    properties: None,
                    neo4j: None,
                    example: None,
                },
                realm: "org".to_string(),
                layer: "foundation".to_string(),
                source_path: PathBuf::from("test.yaml"),
            },
            ParsedNode {
                def: NodeDef {
                    name: "NoIconNode".to_string(),
                    realm: "shared".to_string(),
                    layer: "config".to_string(),
                    node_trait: NodeTrait::Defined,
                    knowledge_tier: None,
                    icon: None, // No icon - should use default
                    description: "Test".to_string(),
                    standard_properties: None,
                    properties: None,
                    neo4j: None,
                    example: None,
                },
                realm: "shared".to_string(),
                layer: "config".to_string(),
                source_path: PathBuf::from("test2.yaml"),
            },
        ];

        let output = render_icons(&nodes).unwrap();

        // v0.12.5: Dual format icons
        assert!(output.contains("TestNode: { web: 'rocket', terminal: '🚀' }"));
        assert!(output.contains("NoIconNode: { web: 'circle', terminal: '●' }")); // Default icon from NodeIcon::default()

        // Count
        assert!(output.contains("NODE ICONS (2 nodes)"));
    }
}
