// ─────────────────────────────────────────────────────────────────────────────
// Generator
// ─────────────────────────────────────────────────────────────────────────────

pub struct ColorsGenerator;

impl super::Generator for ColorsGenerator {
    fn name(&self) -> &'static str {
        "colors"
    }

    fn generate(&self, root: &Path) -> crate::Result<String> {
        let doc = organizing::load_organizing(root)?;
        render_colors(&doc)
    }
}

/// Build template data from organizing principles and render.
fn render_colors(doc: &OrganizingDoc) -> crate::Result<String> {
    // Extract realms
    let realms: Vec<ColorEntry> = doc
        .realms
        .iter()
        .map(|r| ColorEntry {
            key: r.key.clone(),
            color: r.color.clone(),
            display_name: r.display_name.clone(),
        })
        .collect();

    // Extract layers (nested under realms), deduplicated by key
    // Multiple realms may have layers with the same key (e.g., 'config' in shared and org)
    // We keep only unique keys (first occurrence wins)
    let mut seen_layers = std::collections::HashSet::new();
    let layers: Vec<ColorEntry> = doc
        .realms
        .iter()
        .flat_map(|r| &r.layers)
        .filter(|l| seen_layers.insert(l.key.clone()))
        .map(|l| ColorEntry {
            key: l.key.clone(),
            color: l.color.clone(),
            display_name: l.display_name.clone(),
        })
        .collect();

    // v0.17.3 (ADR-036): traits extraction removed, provenance is per-instance

    // Extract arc families
    let arc_families: Vec<ColorEntry> = doc
        .arc_families
        .iter()
        .map(|e| ColorEntry {
            key: e.key.clone(),
            color: e.color.clone(),
            display_name: e.display_name.clone(),
        })
        .collect();

    let data = TemplateData {
        version: doc.version.clone(),
        realms,
        layers,
        arc_families,
    };

    // Render MiniJinja template
    let mut env = Environment::new();
    env.add_template("colors.ts", COLORS_TEMPLATE)
        .map_err(|e| crate::NovaNetError::Generator {
            generator: "colors".to_string(),
            detail: format!("template parse error: {e}"),
        })?;

    let tmpl = env
        .get_template("colors.ts")
        .map_err(|e| crate::NovaNetError::Generator {
            generator: "colors".to_string(),
            detail: format!("template not found: {e}"),
        })?;

    let output = tmpl
        .render(context! {
            version => data.version,
            realms => data.realms,
            layers => data.layers,
            // v0.17.3 (ADR-036): traits removed, provenance is per-instance
            arc_families => data.arc_families,
        })
        .map_err(|e| crate::NovaNetError::Generator {
            generator: "colors".to_string(),
            detail: format!("template render error: {e}"),
        })?;

    Ok(output)
}

// ─────────────────────────────────────────────────────────────────────────────
// Hex to RGB helper (for TUI)
// ─────────────────────────────────────────────────────────────────────────────

/// Parse a hex color string to RGB tuple.
/// Supports "#RRGGBB" and "RRGGBB" formats.
pub fn hex_to_rgb(hex: &str) -> Option<(u8, u8, u8)> {
    let hex = hex.trim_start_matches('#');
    if hex.len() != 6 {
        return None;
    }
    let r = u8::from_str_radix(&hex[0..2], 16).ok()?;
    let g = u8::from_str_radix(&hex[2..4], 16).ok()?;
    let b = u8::from_str_radix(&hex[4..6], 16).ok()?;
    Some((r, g, b))
}

