//! Generate OF_CLASS wiring Cypher statements (instance → Class bridge).
//!
//! v11.8 (ADR-023): Kind → Class, OF_KIND → OF_CLASS
//!
//! For every node type in the schema, generates a MERGE statement that links
//! all existing instances of that type to their Class node via `OF_CLASS`.
//! Replaces the v8 `99-autowire-subcategories.cypher` (IN_SUBCATEGORY → OF_CLASS).
//!
//! Output target: `packages/db/seed/99-autowire-classes.cypher`

use crate::parsers::yaml_node;
use std::collections::BTreeMap;
use std::fmt::Write;
use std::path::Path;

// ─────────────────────────────────────────────────────────────────────────────
// Constants
// ─────────────────────────────────────────────────────────────────────────────

/// Fixed realm order matching the seed file convention (v10.6: 2 realms).
const REALM_ORDER: &[&str] = &["shared", "org"];

// ─────────────────────────────────────────────────────────────────────────────
// Generator
// ─────────────────────────────────────────────────────────────────────────────

pub struct AutowireGenerator;

impl super::Generator for AutowireGenerator {
    fn name(&self) -> &'static str {
        "autowire"
    }

    fn generate(&self, root: &Path) -> crate::Result<String> {
        let nodes = yaml_node::load_all_nodes(root)?;
        generate_autowire(&nodes)
    }
}

fn generate_autowire(nodes: &[yaml_node::ParsedNode]) -> crate::Result<String> {
    // Group by realm → layer → sorted node names
    let mut realm_map: BTreeMap<String, BTreeMap<String, Vec<String>>> = BTreeMap::new();

    for node in nodes {
        realm_map
            .entry(node.realm.clone())
            .or_default()
            .entry(node.layer.clone())
            .or_default()
            .push(node.def.name.clone());
    }

    // Sort node names within each layer
    for layers in realm_map.values_mut() {
        for names in layers.values_mut() {
            names.sort();
        }
    }

    let mut out = String::with_capacity(8 * 1024);

    // Header
    writeln!(
        out,
        "// Auto-wire OF_CLASS relationships (v11.8 instance → Class bridge)"
    )
    .unwrap();
    writeln!(
        out,
        "// ═══════════════════════════════════════════════════════════════════════════════"
    )
    .unwrap();
    writeln!(
        out,
        "// Runs AFTER all other seeds to connect instance nodes to their Class nodes."
    )
    .unwrap();
    writeln!(
        out,
        "// Idempotent: Uses MERGE to allow repeated execution."
    )
    .unwrap();
    writeln!(out, "//").unwrap();
    writeln!(out, "// AUTO-GENERATED by novanet v0.13.0 — DO NOT EDIT").unwrap();
    writeln!(out, "// Total: {count} node types", count = nodes.len()).unwrap();
    writeln!(
        out,
        "// ═══════════════════════════════════════════════════════════════════════════════"
    )
    .unwrap();
    writeln!(out).unwrap();

    for &realm_key in REALM_ORDER {
        let Some(layers_map) = realm_map.get(realm_key) else {
            return Err(crate::NovaNetError::Validation(format!(
                "realm '{realm_key}' has no nodes — expected nodes under models/node-classes/{realm_key}/"
            )));
        };

        // Realm header
        writeln!(
            out,
            "// ─────────────────────────────────────────────────────────────────────────────"
        )
        .unwrap();
        writeln!(out, "// {} REALM", realm_key.to_uppercase()).unwrap();
        writeln!(
            out,
            "// ─────────────────────────────────────────────────────────────────────────────"
        )
        .unwrap();
        writeln!(out).unwrap();

        // Sorted layers within realm
        let mut layers: Vec<(&String, &Vec<String>)> = layers_map.iter().collect();
        layers.sort_by_key(|(k, _)| k.as_str());

        for (layer_key, node_names) in layers {
            writeln!(
                out,
                "// {realm} > {layer} ({count} type{s})",
                realm = {
                    let mut c = realm_key.chars();
                    match c.next() {
                        None => String::new(),
                        Some(f) => f.to_uppercase().to_string() + c.as_str(),
                    }
                },
                layer = {
                    let mut c = layer_key.chars();
                    match c.next() {
                        None => String::new(),
                        Some(f) => f.to_uppercase().to_string() + c.as_str(),
                    }
                },
                count = node_names.len(),
                s = if node_names.len() == 1 { "" } else { "s" },
            )
            .unwrap();

            for name in node_names {
                writeln!(out, "MATCH (n:{name})").unwrap();
                // v11.8: :Class (was :Kind)
                writeln!(out, "MATCH (c:Class {{label: '{name}'}})").unwrap();
                writeln!(out, "MERGE (n)-[:OF_CLASS]->(c);").unwrap();
                writeln!(out).unwrap();
            }
        }
    }

    // Verification query
    writeln!(
        out,
        "// ═══════════════════════════════════════════════════════════════════════════════"
    )
    .unwrap();
    writeln!(out, "// VERIFICATION QUERY").unwrap();
    writeln!(
        out,
        "// ═══════════════════════════════════════════════════════════════════════════════"
    )
    .unwrap();
    writeln!(out, "// Run this to verify the wiring was successful:").unwrap();
    writeln!(out, "//").unwrap();
    // v11.8: :Class, [:HAS_CLASS], [:OF_CLASS] (was :Kind, [:HAS_KIND], [:OF_KIND])
    writeln!(
        out,
        "// MATCH (r:Realm)-[:HAS_LAYER]->(l:Layer)-[:HAS_CLASS]->(c:Class)"
    )
    .unwrap();
    writeln!(out, "// OPTIONAL MATCH (c)<-[rel:OF_CLASS]-(n)").unwrap();
    writeln!(
        out,
        "// WITH r.key AS realm, l.key AS layer, c.label AS class, count(rel) AS instanceCount"
    )
    .unwrap();
    writeln!(out, "// ORDER BY realm, layer, instanceCount DESC").unwrap();
    writeln!(out, "// RETURN realm, layer, class, instanceCount;").unwrap();

    Ok(out)
}

