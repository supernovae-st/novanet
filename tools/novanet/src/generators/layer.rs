//! Generate `layers.ts` via MiniJinja template (YAML → TypeScript).
//!
//! Reads all 44 YAML node definitions, groups by realm and layer,
//! and produces `packages/core/src/graph/layers.ts`.
//! Replaces `SubcategoryGenerator.ts` from @novanet/schema-tools.
//!
//! Output target: `packages/core/src/graph/layers.ts`

use crate::parsers::yaml_node;
use minijinja::{Environment, context};
use serde::Serialize;
use std::collections::{BTreeMap, HashSet};
use std::path::Path;

// ─────────────────────────────────────────────────────────────────────────────
// Template data structures
// ─────────────────────────────────────────────────────────────────────────────

/// A realm group for the template (project, global, shared).
#[derive(Debug, Serialize)]
struct TemplateRealm {
    key: String,
    /// Title case for JSDoc comments (e.g. "Project").
    label: String,
    /// Uppercase for section headers (e.g. "PROJECT").
    label_upper: String,
    node_count: usize,
    layers: Vec<TemplateLayer>,
}

/// A layer group within a realm.
#[derive(Debug, Serialize)]
struct TemplateLayer {
    key: String,
    nodes: Vec<String>,
    node_count: usize,
    realm_key: String,
}

// ─────────────────────────────────────────────────────────────────────────────
// Realm ordering
// ─────────────────────────────────────────────────────────────────────────────

/// Fixed realm order matching taxonomy.yaml (v10.6: 2 realms).
const REALM_ORDER: &[&str] = &["global", "tenant"];

// ─────────────────────────────────────────────────────────────────────────────
// MiniJinja template
// ─────────────────────────────────────────────────────────────────────────────

const LAYERS_TEMPLATE: &str = r##"// packages/core/src/graph/layers.ts
// NODE_LAYERS mapping all {{ total_nodes }} node types to their layers
// AUTO-GENERATED by novanet v{{ version }} — DO NOT EDIT
// Run: novanet schema generate

import type { NodeType, Realm } from '../types/nodes.js';
import type { Layer } from './types.js';
import { KIND_META } from '../types/nodes.js';

// =============================================================================
// NODE_LAYERS - Maps each NodeType to its layer
// =============================================================================

/**
 * Maps each NodeType to its layer within its realm.
 * AUTO-GENERATED from models/node-kinds/ folder structure.
 *
 * Layers by realm:
{%- for realm in realms %}
 * - {{ realm.label }}: {{ realm.layers | map(attribute="key") | join(", ") }}
{%- endfor %}
 */
export const NODE_LAYERS: Record<NodeType, Layer> = {
{%- for realm in realms %}
  // ═══════════════════════════════════════════════════════════════════════════
  // {{ realm.label_upper }} REALM ({{ realm.node_count }} node{{ "s" if realm.node_count != 1 else "" }})
  // ═══════════════════════════════════════════════════════════════════════════
{% for layer in realm.layers %}
  // {{ layer.key }} ({{ layer.node_count }} node{{ "s" if layer.node_count != 1 else "" }}) - matches models/node-kinds/{{ layer.realm_key }}/{{ layer.key }}/
{%- for node in layer.nodes %}
  {{ node }}: '{{ layer.key }}',
{%- endfor %}
{% endfor %}
{%- endfor %}
};

// =============================================================================
// HELPER FUNCTIONS
// =============================================================================

/**
 * Get the layer for a node type
 * @param nodeType - The node type to look up
 * @returns The layer for the node type
 */
export function getLayer(nodeType: NodeType): Layer {
  return NODE_LAYERS[nodeType];
}

/**
 * Get all node types in a layer
 * @param layer - The layer to look up
 * @returns Array of node types belonging to the layer
 */
export function getNodeTypesByLayer(layer: Layer): NodeType[] {
  return (Object.entries(NODE_LAYERS) as [NodeType, Layer][])
    .filter(([, l]) => l === layer)
    .map(([nodeType]) => nodeType);
}

/**
 * Get all node types in a specific realm AND layer
 * @param realm - The realm to filter by
 * @param layer - The layer to filter by
 * @returns Array of node types belonging to both the realm and layer
 */
export function getNodeTypesByRealmAndLayer(realm: Realm, layer: Layer): NodeType[] {
  return (Object.entries(KIND_META) as [NodeType, { realm: Realm; layer: Layer }][])
    .filter(([, meta]) => meta.realm === realm && meta.layer === layer)
    .map(([nodeType]) => nodeType);
}
"##;

// ─────────────────────────────────────────────────────────────────────────────
// Generator
// ─────────────────────────────────────────────────────────────────────────────

pub struct LayerGenerator;

impl super::Generator for LayerGenerator {
    fn name(&self) -> &'static str {
        "layers"
    }

    fn generate(&self, root: &Path) -> crate::Result<String> {
        let nodes = yaml_node::load_all_nodes(root)?;
        render_layers(&nodes)
    }
}

/// Group parsed nodes by realm → layer → sorted node names, then render template.
/// De-duplicates nodes that appear in multiple realms (e.g., Entity in both org and project).
fn render_layers(nodes: &[yaml_node::ParsedNode]) -> crate::Result<String> {
    // Build realm → layer → [node_name] map
    let mut realm_map: BTreeMap<String, BTreeMap<String, Vec<String>>> = BTreeMap::new();

    // Track seen node names to de-duplicate (v10.5: Entity exists in both org and project)
    let mut seen_names: HashSet<String> = HashSet::new();

    // Process in realm order to ensure consistent deduplication (global first)
    for &realm_key in REALM_ORDER {
        for node in nodes.iter().filter(|n| n.realm == realm_key) {
            // Skip if we've already seen this node name in another realm
            if seen_names.contains(&node.def.name) {
                continue;
            }
            seen_names.insert(node.def.name.clone());

            realm_map
                .entry(node.realm.clone())
                .or_default()
                .entry(node.layer.clone())
                .or_default()
                .push(node.def.name.clone());
        }
    }

    // Sort node names within each layer
    for layers in realm_map.values_mut() {
        for node_names in layers.values_mut() {
            node_names.sort();
        }
    }

    // Build template data in fixed realm order
    let mut realms: Vec<TemplateRealm> = Vec::new();

    for &realm_key in REALM_ORDER {
        let Some(layers_map) = realm_map.get(realm_key) else {
            return Err(crate::NovaNetError::Validation(format!(
                "realm '{realm_key}' has no nodes — expected nodes under models/node-kinds/{realm_key}/"
            )));
        };

        let mut layers: Vec<TemplateLayer> = layers_map
            .iter()
            .map(|(layer_key, node_names)| TemplateLayer {
                key: layer_key.clone(),
                nodes: node_names.clone(),
                node_count: node_names.len(),
                realm_key: realm_key.to_string(),
            })
            .collect();

        // Sort layers alphabetically within realm
        layers.sort_by(|a, b| a.key.cmp(&b.key));

        let node_count: usize = layers.iter().map(|l| l.node_count).sum();

        realms.push(TemplateRealm {
            key: realm_key.to_string(),
            label: {
                let mut c = realm_key.chars();
                match c.next() {
                    None => String::new(),
                    Some(f) => f.to_uppercase().to_string() + c.as_str(),
                }
            },
            label_upper: realm_key.to_uppercase(),
            node_count,
            layers,
        });
    }

    let total_nodes: usize = realms.iter().map(|r| r.node_count).sum();

    // Render MiniJinja template
    let mut env = Environment::new();
    env.add_template("layers.ts", LAYERS_TEMPLATE)
        .map_err(|e| crate::NovaNetError::Generator {
            generator: "layers".to_string(),
            detail: format!("template parse error: {e}"),
        })?;

    let tmpl = env
        .get_template("layers.ts")
        .map_err(|e| crate::NovaNetError::Generator {
            generator: "layers".to_string(),
            detail: format!("template not found: {e}"),
        })?;

    let output = tmpl
        .render(context! {
            version => "10.6.0",
            total_nodes => total_nodes,
            realms => realms,
        })
        .map_err(|e| crate::NovaNetError::Generator {
            generator: "layers".to_string(),
            detail: format!("template render error: {e}"),
        })?;

    Ok(output)
}

