pub struct TuiColorsGenerator;

impl super::Generator for TuiColorsGenerator {
    fn name(&self) -> &'static str {
        "tui_colors"
    }

    fn generate(&self, root: &Path) -> crate::Result<String> {
        // Load colors from individual YAML files
        let doc = organizing::load_organizing(root)?;

        // Load terminal palette for 256/16 color fallbacks
        let taxonomy = taxonomy::load_taxonomy_from_files(root)?;
        let terminal = taxonomy.terminal.unwrap_or_else(default_terminal_palette);

        render_tui_colors(&doc, &terminal)
    }
}

/// Default terminal palette when taxonomy.yaml doesn't have one.
fn default_terminal_palette() -> TerminalPalette {
    let mut palette_256 = HashMap::new();
    let mut palette_16 = HashMap::new();

    // Realms
    palette_256.insert("shared".to_string(), 37);
    palette_256.insert("org".to_string(), 141);
    palette_16.insert("shared".to_string(), 6); // Cyan
    palette_16.insert("org".to_string(), 5); // Magenta

    // Layers
    for (key, idx_256, idx_16) in [
        ("config", 244, 8),
        ("locale", 244, 8),
        ("geography", 43, 2),
        ("knowledge", 141, 5),
        ("foundation", 33, 4),
        ("structure", 45, 6),
        ("semantic", 208, 3),
        ("instruction", 178, 11),
        ("output", 41, 2),
    ] {
        palette_256.insert(key.to_string(), idx_256);
        palette_16.insert(key.to_string(), idx_16);
    }

    // Traits
    for (key, idx_256, idx_16) in [
        ("defined", 33, 4),
        ("authored", 41, 2),
        ("imported", 141, 5),
        ("generated", 136, 3),
        ("retrieved", 141, 5),
    ] {
        palette_256.insert(key.to_string(), idx_256);
        palette_16.insert(key.to_string(), idx_16);
    }

    // Arc families
    for (key, idx_256, idx_16) in [
        ("ownership", 33, 4),
        ("localization", 41, 2),
        ("semantic", 208, 3),
        ("generation", 141, 5),
        ("mining", 205, 13),
    ] {
        palette_256.insert(format!("{}_arc", key), idx_256);
        palette_16.insert(format!("{}_arc", key), idx_16);
    }

    TerminalPalette {
        palette_256,
        palette_16,
    }
}

/// Map 16-color index to Color variant name.
fn color_16_name(idx: u8) -> &'static str {
    match idx {
        0 => "Black",
        1 => "Red",
        2 => "Green",
        3 => "Yellow",
        4 => "Blue",
        5 => "Magenta",
        6 => "Cyan",
        7 => "Gray",
        8 => "DarkGray",
        9 => "LightRed",
        10 => "LightGreen",
        11 => "LightYellow",
        12 => "LightBlue",
        13 => "LightMagenta",
        14 => "LightCyan",
        15 => "White",
        _ => "White",
    }
}

/// Map trait key to Unicode border character.
fn trait_border_char(key: &str) -> &'static str {
    match key {
        "defined" => "─",
        "authored" => "┄",
        "imported" => "┈",
        "generated" => "═",
        "retrieved" => "┅",
        _ => "─",
    }
}

/// Build template data from organizing doc and terminal palette.
fn render_tui_colors(doc: &OrganizingDoc, terminal: &TerminalPalette) -> crate::Result<String> {
    // Extract realms
    let realms: Vec<ColorEntry> = doc
        .realms
        .iter()
        .map(|r| {
            let idx_256 = terminal.palette_256.get(&r.key).copied().unwrap_or(7);
            let idx_16 = terminal.palette_16.get(&r.key).copied().unwrap_or(7);
            ColorEntry {
                key: r.key.clone(),
                key_upper: r.key.to_uppercase(),
                color_hex: r.color.clone(),
                color_256: idx_256,
                color_16: color_16_name(idx_16).to_string(),
            }
        })
        .collect();

    // Extract layers (deduplicated)
    let mut seen_layers = std::collections::HashSet::new();
    let layers: Vec<ColorEntry> = doc
        .realms
        .iter()
        .flat_map(|r| &r.layers)
        .filter(|l| seen_layers.insert(l.key.clone()))
        .map(|l| {
            let idx_256 = terminal.palette_256.get(&l.key).copied().unwrap_or(7);
            let idx_16 = terminal.palette_16.get(&l.key).copied().unwrap_or(7);
            ColorEntry {
                key: l.key.clone(),
                key_upper: l.key.to_uppercase(),
                color_hex: l.color.clone(),
                color_256: idx_256,
                color_16: color_16_name(idx_16).to_string(),
            }
        })
        .collect();

    // Extract traits
    let traits: Vec<TraitEntry> = doc
        .traits
        .iter()
        .map(|t| {
            let idx_256 = terminal.palette_256.get(&t.key).copied().unwrap_or(7);
            let idx_16 = terminal.palette_16.get(&t.key).copied().unwrap_or(7);
            TraitEntry {
                key: t.key.clone(),
                key_upper: t.key.to_uppercase(),
                color_hex: t.color.clone(),
                color_256: idx_256,
                color_16: color_16_name(idx_16).to_string(),
                border_char: trait_border_char(&t.key).to_string(),
            }
        })
        .collect();

    // Extract arc families
    let arc_families: Vec<ColorEntry> = doc
        .arc_families
        .iter()
        .map(|af| {
            // Try with _arc suffix first, then without
            let palette_key = format!("{}_arc", af.key);
            let idx_256 = terminal
                .palette_256
                .get(&palette_key)
                .or_else(|| terminal.palette_256.get(&af.key))
                .copied()
                .unwrap_or(7);
            let idx_16 = terminal
                .palette_16
                .get(&palette_key)
                .or_else(|| terminal.palette_16.get(&af.key))
                .copied()
                .unwrap_or(7);
            ColorEntry {
                key: af.key.clone(),
                key_upper: af.key.to_uppercase(),
                color_hex: af.color.clone(),
                color_256: idx_256,
                color_16: color_16_name(idx_16).to_string(),
            }
        })
        .collect();

    let data = TemplateData {
        version: doc.version.clone(),
        realms,
        layers,
        traits,
        arc_families,
    };

    // Render MiniJinja template
    let mut env = Environment::new();
    env.add_template("tui_colors.rs", TUI_COLORS_TEMPLATE)
        .map_err(|e| crate::NovaNetError::Generator {
            generator: "tui_colors".to_string(),
            detail: format!("template parse error: {e}"),
        })?;

    let tmpl = env
        .get_template("tui_colors.rs")
        .map_err(|e| crate::NovaNetError::Generator {
            generator: "tui_colors".to_string(),
            detail: format!("template not found: {e}"),
        })?;

    let output = tmpl
        .render(context! {
            version => data.version,
            realms => data.realms,
            layers => data.layers,
            traits => data.traits,
            arc_families => data.arc_families,
        })
        .map_err(|e| crate::NovaNetError::Generator {
            generator: "tui_colors".to_string(),
            detail: format!("template render error: {e}"),
        })?;

    Ok(output)
}

