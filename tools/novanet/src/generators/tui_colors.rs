/// Color entry with hex, 256, and 16-color fallbacks.
#[derive(Debug, Serialize)]
struct ColorEntry {
    key: String,
    key_upper: String,
    color_hex: String,
    color_256: u8,
    color_16: String,
}

/// Trait entry with color and border style.
#[derive(Debug, Serialize)]
struct TraitEntry {
    key: String,
    key_upper: String,
    color_hex: String,
    color_256: u8,
    color_16: String,
    border_char: String,
}

/// All color data for the template.
#[derive(Debug, Serialize)]
struct TemplateData {
    version: String,
    realms: Vec<ColorEntry>,
    layers: Vec<ColorEntry>,
    traits: Vec<TraitEntry>,
    arc_families: Vec<ColorEntry>,
}

