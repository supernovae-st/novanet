//! Generate `visual-encoding.ts` via MiniJinja template.
//!
//! Reads `visual-encoding.yaml` and produces TypeScript types + helpers for:
//! - Channel mapping (node: fill_color → layer, etc.)
//! - Node/arc states (default, focused, hover, selected, filtered)
//! - Trait border styles (CSS + Unicode for TUI)
//! - Scope stroke styles (intra/cross realm)
//! - Cardinality arrow heads
//! - Class icons (Lucide icon mapping) — v11.8: renamed from Kind icons
//! - Animation presets
//! - Accessibility settings
//!
//! Output target: `packages/core/src/graph/visual-encoding.ts`

use crate::parsers::visual_encoding::{self, VisualEncodingDoc};
