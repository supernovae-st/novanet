pub struct VisualEncodingGenerator;

impl super::Generator for VisualEncodingGenerator {
    fn name(&self) -> &'static str {
        "visual_encoding"
    }

    fn generate(&self, root: &Path) -> crate::Result<String> {
        let doc = visual_encoding::load_visual_encoding(root)?;
        render_visual_encoding(&doc)
    }
}

fn render_visual_encoding(doc: &VisualEncodingDoc) -> crate::Result<String> {
    // Channel mapping
    let channel_mapping = TemplateChannelMapping {
        node_fill_color: doc.channel_mapping.node.fill_color.clone(),
        node_border_style: doc.channel_mapping.node.border_style.clone(),
        node_border_color: doc.channel_mapping.node.border_color.clone(),
        node_icon: doc.channel_mapping.node.icon.clone(),
        node_spatial_grouping: doc.channel_mapping.node.spatial_grouping.clone(),
        arc_stroke_color: doc.channel_mapping.arc.stroke_color.clone(),
        arc_stroke_style: doc.channel_mapping.arc.stroke_style.clone(),
        arc_arrow_head: doc.channel_mapping.arc.arrow_head.clone(),
        arc_label_position: doc.channel_mapping.arc.label_position.clone(),
    };

    // Node states (sorted by key for deterministic output)
    let mut node_states: Vec<TemplateNodeState> = doc
        .node_states
        .iter()
        .map(|(key, state)| TemplateNodeState {
            key: key.clone(),
            opacity: state.opacity,
            scale: state.scale,
            shadow: state.shadow.clone().unwrap_or_default(),
            ring: state.ring.clone().unwrap_or_default(),
        })
        .collect();
    node_states.sort_by(|a, b| a.key.cmp(&b.key));

    // Arc states (sorted)
    let mut arc_states: Vec<TemplateArcState> = doc
        .arc_states
        .iter()
        .map(|(key, state)| TemplateArcState {
            key: key.clone(),
            opacity: state.opacity,
            stroke_width: state.stroke_width,
            animated: state.animated.unwrap_or(false),
            label_visible: state.label_visible.unwrap_or(false),
        })
        .collect();
    arc_states.sort_by(|a, b| a.key.cmp(&b.key));

    // Trait borders (sorted)
    let mut trait_borders: Vec<TemplateTraitBorder> = doc
        .trait_borders
        .iter()
        .map(|(key, border)| TemplateTraitBorder {
            key: key.clone(),
            css_style: border.css_style.clone(),
            css_width: border.css_width.clone(),
            css_dash_array: border.css_dash_array.clone().unwrap_or_default(),
            css_corner_radius: border.css_corner_radius.clone().unwrap_or_default(),
            unicode_char: border.unicode_char.clone(),
            unicode_style: border.unicode_style.clone(),
            description: escape_js_string(&border.description),
        })
        .collect();
    trait_borders.sort_by(|a, b| a.key.cmp(&b.key));

    // Scope strokes (sorted)
    let mut scope_strokes: Vec<TemplateScopeStroke> = doc
        .scope_strokes
        .iter()
        .map(|(key, stroke)| TemplateScopeStroke {
            key: key.clone(),
            stroke_style: stroke.stroke_style.clone(),
            opacity: stroke.opacity,
            description: escape_js_string(&stroke.description),
        })
        .collect();
    scope_strokes.sort_by(|a, b| a.key.cmp(&b.key));

    // Cardinality arrows (sorted)
    let mut cardinality_arrows: Vec<TemplateCardinalityArrow> = doc
        .cardinality_arrows
        .iter()
        .map(|(key, arrow)| TemplateCardinalityArrow {
            key: key.clone(),
            arrow_head: arrow.arrow_head.clone(),
            arrow_end: arrow.arrow_end.clone(),
            description: escape_js_string(&arrow.description),
        })
        .collect();
    cardinality_arrows.sort_by(|a, b| a.key.cmp(&b.key));

    // Class icons (sorted by class name) — v11.8: renamed from Kind icons
    let mut class_icons: Vec<TemplateClassIcon> = doc
        .class_icons
        .iter()
        .map(|(kind, icon)| TemplateClassIcon {
            kind: kind.clone(),
            icon: icon.clone(),
        })
        .collect();
    class_icons.sort_by(|a, b| a.kind.cmp(&b.kind));

    // Animations (sorted)
    let mut animations: Vec<TemplateAnimation> = doc
        .animations
        .iter()
        .map(|(key, anim)| TemplateAnimation {
            key: key.clone(),
            duration_ms: anim.duration_ms,
            easing: anim.easing.clone(),
            stagger_ms: anim.stagger_ms.unwrap_or(0),
        })
        .collect();
    animations.sort_by(|a, b| a.key.cmp(&b.key));

    // Accessibility
    let accessibility = TemplateAccessibility {
        min_contrast_ratio: doc.accessibility.min_contrast_ratio,
        use_patterns: doc.accessibility.use_patterns,
        use_icons: doc.accessibility.use_icons,
        use_borders: doc.accessibility.use_borders,
        focus_ring_width: doc.accessibility.focus_ring_width.clone(),
        focus_ring_color: doc.accessibility.focus_ring_color.clone(),
        focus_ring_offset: doc.accessibility.focus_ring_offset.clone(),
    };

    // Icon categories (v10.6)
    let icon_categories = build_icon_categories(doc);

    // Render template
    let mut env = Environment::new();
    env.add_template("visual-encoding.ts", VISUAL_ENCODING_TEMPLATE)
        .map_err(|e| crate::NovaNetError::Generator {
            generator: "visual_encoding".to_string(),
            detail: format!("template parse error: {e}"),
        })?;

    let tmpl =
        env.get_template("visual-encoding.ts")
            .map_err(|e| crate::NovaNetError::Generator {
                generator: "visual_encoding".to_string(),
                detail: format!("template not found: {e}"),
            })?;

    let output = tmpl
        .render(context! {
            version => doc.version,
            channel_mapping => channel_mapping,
            node_states => node_states,
            arc_states => arc_states,
            trait_borders => trait_borders,
            scope_strokes => scope_strokes,
            cardinality_arrows => cardinality_arrows,
            class_icons => class_icons,
            animations => animations,
            accessibility => accessibility,
            icon_categories => icon_categories,
        })
        .map_err(|e| crate::NovaNetError::Generator {
            generator: "visual_encoding".to_string(),
            detail: format!("template render error: {e}"),
        })?;

    Ok(output)
}

/// Build icon categories from the Icons section of visual-encoding.yaml.
fn build_icon_categories(doc: &VisualEncodingDoc) -> Vec<TemplateIconCategory> {
    let Some(ref icons) = doc.icons else {
        return Vec::new();
    };

    let mut categories = Vec::new();

    // Helper to convert HashMap to sorted Vec of TemplateIcon
    let to_icons =
        |map: &std::collections::HashMap<String, crate::parsers::visual_encoding::Icon>| {
            let mut items: Vec<TemplateIcon> = map
                .iter()
                .map(|(key, icon)| TemplateIcon {
                    key: key.clone(),
                    web: icon.web.clone(),
                    terminal: icon.terminal.clone(),
                    description: escape_js_string(&icon.description),
                })
                .collect();
            items.sort_by(|a, b| a.key.cmp(&b.key));
            items
        };

    // Add each category in a defined order
    if !icons.realms.is_empty() {
        categories.push(TemplateIconCategory {
            category: "realms".to_string(),
            icons: to_icons(&icons.realms),
        });
    }

    if !icons.layers.is_empty() {
        categories.push(TemplateIconCategory {
            category: "layers".to_string(),
            icons: to_icons(&icons.layers),
        });
    }

    if !icons.traits.is_empty() {
        categories.push(TemplateIconCategory {
            category: "traits".to_string(),
            icons: to_icons(&icons.traits),
        });
    }

    if !icons.arc_families.is_empty() {
        categories.push(TemplateIconCategory {
            category: "arc_families".to_string(),
            icons: to_icons(&icons.arc_families),
        });
    }

    if !icons.states.is_empty() {
        categories.push(TemplateIconCategory {
            category: "states".to_string(),
            icons: to_icons(&icons.states),
        });
    }

    if !icons.navigation.is_empty() {
        categories.push(TemplateIconCategory {
            category: "navigation".to_string(),
            icons: to_icons(&icons.navigation),
        });
    }

    if !icons.quality.is_empty() {
        categories.push(TemplateIconCategory {
            category: "quality".to_string(),
            icons: to_icons(&icons.quality),
        });
    }

    if !icons.modes.is_empty() {
        categories.push(TemplateIconCategory {
            category: "modes".to_string(),
            icons: to_icons(&icons.modes),
        });
    }

    categories
}

