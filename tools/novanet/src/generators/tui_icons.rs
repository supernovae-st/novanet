//! Generate `icons.generated.rs` for TUI — compile-time icon constants.
//!
//! Reads `visual-encoding.yaml` → `icons:` section and produces Rust constants.
//! This eliminates runtime YAML loading and provides compile-time verification.
//!
//! Output target: `tools/novanet/src/tui/icons.generated.rs`
//!
//! Categories: realms, layers, traits, arc_families, states, navigation, quality, modes

#![allow(clippy::needless_raw_string_hashes)]

use crate::parsers::visual_encoding::{self, VisualEncodingDoc};
use std::path::Path;

// ─────────────────────────────────────────────────────────────────────────────
// Generator
// ─────────────────────────────────────────────────────────────────────────────

pub struct TuiIconsGenerator;

impl super::Generator for TuiIconsGenerator {
    fn name(&self) -> &'static str {
        "tui_icons"
    }

    fn generate(&self, root: &Path) -> crate::Result<String> {
        let doc = visual_encoding::load_visual_encoding(root)?;
        render_tui_icons(&doc)
    }
}

fn render_tui_icons(doc: &VisualEncodingDoc) -> crate::Result<String> {
    let mut output = String::new();

    // Header
    output.push_str(&format!(
        r#"//! TUI icons from visual-encoding.yaml (SINGLE SOURCE OF TRUTH)
//! AUTO-GENERATED by novanet v{} — DO NOT EDIT
//! Run: cargo run -- schema generate

use std::collections::HashMap;

/// Icon with web (Lucide) and terminal (Unicode) variants.
#[derive(Debug, Clone)]
pub struct IconDef {{
    pub web: &'static str,
    pub terminal: &'static str,
    pub description: &'static str,
}}

impl IconDef {{
    const fn new(web: &'static str, terminal: &'static str, description: &'static str) -> Self {{
        Self {{ web, terminal, description }}
    }}
}}

"#,
        doc.version
    ));

    // Generate constants for each category
    let Some(ref icons) = doc.icons else {
        output.push_str("// No icons defined in visual-encoding.yaml\n");
        return Ok(output);
    };

    // Helper to generate a category
    let mut generate_category = |name: &str,
                                 category: &std::collections::HashMap<
        String,
        crate::parsers::visual_encoding::Icon,
    >| {
        let upper_name = name.to_uppercase();
        let mut items: Vec<_> = category.iter().collect();
        items.sort_by(|a, b| a.0.cmp(b.0));

        output.push_str(&format!(
            "// =============================================================================\n// {} ICONS ({})\n// =============================================================================\n\n",
            upper_name,
            items.len()
        ));

        for (key, icon) in &items {
            let const_name = format!("{}_{}", upper_name, key.to_uppercase().replace('-', "_"));
            output.push_str(&format!(
                "pub const {}: IconDef = IconDef::new(\"{}\", \"{}\", \"{}\");\n",
                const_name,
                icon.web,
                icon.terminal,
                icon.description.replace('"', "\\\"")
            ));
        }
        output.push('\n');
    };

    generate_category("realms", &icons.realms);
    generate_category("layers", &icons.layers);
    generate_category("traits", &icons.traits);
    generate_category("arc_families", &icons.arc_families);
    generate_category("states", &icons.states);
    generate_category("navigation", &icons.navigation);
    generate_category("quality", &icons.quality);
    generate_category("modes", &icons.modes);

    // Generate lookup functions
    output.push_str(
        r#"// =============================================================================
// LOOKUP FUNCTIONS
// =============================================================================

/// Build a HashMap for a category (called once at startup).
macro_rules! icon_map {
    ($($key:literal => $val:expr),* $(,)?) => {{
        let mut map = HashMap::new();
        $(map.insert($key, $val);)*
        map
    }};
}

/// Get all realm icons.
pub fn realm_icons() -> HashMap<&'static str, IconDef> {
    icon_map! {
"#,
    );

    // Realms
    let mut items: Vec<_> = icons.realms.iter().collect();
    items.sort_by(|a, b| a.0.cmp(b.0));
    for (key, _) in items {
        let const_name = format!("REALMS_{}", key.to_uppercase().replace('-', "_"));
        output.push_str(&format!("        \"{}\" => {},\n", key, const_name));
    }
    output.push_str("    }\n}\n\n");

    // Layers
    output.push_str("/// Get all layer icons.\npub fn layer_icons() -> HashMap<&'static str, IconDef> {\n    icon_map! {\n");
    let mut items: Vec<_> = icons.layers.iter().collect();
    items.sort_by(|a, b| a.0.cmp(b.0));
    for (key, _) in items {
        let const_name = format!("LAYERS_{}", key.to_uppercase().replace('-', "_"));
        output.push_str(&format!("        \"{}\" => {},\n", key, const_name));
    }
    output.push_str("    }\n}\n\n");

    // Traits
    output.push_str("/// Get all trait icons.\npub fn trait_icons() -> HashMap<&'static str, IconDef> {\n    icon_map! {\n");
    let mut items: Vec<_> = icons.traits.iter().collect();
    items.sort_by(|a, b| a.0.cmp(b.0));
    for (key, _) in items {
        let const_name = format!("TRAITS_{}", key.to_uppercase().replace('-', "_"));
        output.push_str(&format!("        \"{}\" => {},\n", key, const_name));
    }
    output.push_str("    }\n}\n\n");

    // Arc families
    output.push_str("/// Get all arc family icons.\npub fn arc_family_icons() -> HashMap<&'static str, IconDef> {\n    icon_map! {\n");
    let mut items: Vec<_> = icons.arc_families.iter().collect();
    items.sort_by(|a, b| a.0.cmp(b.0));
    for (key, _) in items {
        let const_name = format!("ARC_FAMILIES_{}", key.to_uppercase().replace('-', "_"));
        output.push_str(&format!("        \"{}\" => {},\n", key, const_name));
    }
    output.push_str("    }\n}\n\n");

    // States
    output.push_str("/// Get all state icons.\npub fn state_icons() -> HashMap<&'static str, IconDef> {\n    icon_map! {\n");
    let mut items: Vec<_> = icons.states.iter().collect();
    items.sort_by(|a, b| a.0.cmp(b.0));
    for (key, _) in items {
        let const_name = format!("STATES_{}", key.to_uppercase().replace('-', "_"));
        output.push_str(&format!("        \"{}\" => {},\n", key, const_name));
    }
    output.push_str("    }\n}\n\n");

    // Navigation
    output.push_str("/// Get all navigation icons.\npub fn navigation_icons() -> HashMap<&'static str, IconDef> {\n    icon_map! {\n");
    let mut items: Vec<_> = icons.navigation.iter().collect();
    items.sort_by(|a, b| a.0.cmp(b.0));
    for (key, _) in items {
        let const_name = format!("NAVIGATION_{}", key.to_uppercase().replace('-', "_"));
        output.push_str(&format!("        \"{}\" => {},\n", key, const_name));
    }
    output.push_str("    }\n}\n\n");

    // Quality
    output.push_str("/// Get all quality icons.\npub fn quality_icons() -> HashMap<&'static str, IconDef> {\n    icon_map! {\n");
    let mut items: Vec<_> = icons.quality.iter().collect();
    items.sort_by(|a, b| a.0.cmp(b.0));
    for (key, _) in items {
        let const_name = format!("QUALITY_{}", key.to_uppercase().replace('-', "_"));
        output.push_str(&format!("        \"{}\" => {},\n", key, const_name));
    }
    output.push_str("    }\n}\n\n");

    // Modes
    output.push_str("/// Get all mode icons.\npub fn mode_icons() -> HashMap<&'static str, IconDef> {\n    icon_map! {\n");
    let mut items: Vec<_> = icons.modes.iter().collect();
    items.sort_by(|a, b| a.0.cmp(b.0));
    for (key, _) in items {
        let const_name = format!("MODES_{}", key.to_uppercase().replace('-', "_"));
        output.push_str(&format!("        \"{}\" => {},\n", key, const_name));
    }
    output.push_str("    }\n}\n\n");

    // Convenience functions
    output.push_str(
        r#"// =============================================================================
// CONVENIENCE FUNCTIONS
// =============================================================================

/// Get terminal icon for a realm.
pub fn realm_terminal_icon(key: &str) -> &'static str {
    match key {
"#,
    );
    let mut items: Vec<_> = icons.realms.iter().collect();
    items.sort_by(|a, b| a.0.cmp(b.0));
    for (key, icon) in items {
        output.push_str(&format!("        \"{}\" => \"{}\",\n", key, icon.terminal));
    }
    output.push_str("        _ => \"·\",\n    }\n}\n\n");

    output.push_str(
        r#"/// Get terminal icon for a layer.
pub fn layer_terminal_icon(key: &str) -> &'static str {
    match key {
"#,
    );
    let mut items: Vec<_> = icons.layers.iter().collect();
    items.sort_by(|a, b| a.0.cmp(b.0));
    for (key, icon) in items {
        output.push_str(&format!("        \"{}\" => \"{}\",\n", key, icon.terminal));
    }
    output.push_str("        _ => \"·\",\n    }\n}\n\n");

    output.push_str(
        r#"/// Get terminal icon for a trait.
pub fn trait_terminal_icon(key: &str) -> &'static str {
    match key {
"#,
    );
    let mut items: Vec<_> = icons.traits.iter().collect();
    items.sort_by(|a, b| a.0.cmp(b.0));
    for (key, icon) in items {
        output.push_str(&format!("        \"{}\" => \"{}\",\n", key, icon.terminal));
    }
    output.push_str("        _ => \"·\",\n    }\n}\n\n");

    output.push_str(
        r#"/// Get terminal icon for a navigation action.
pub fn navigation_terminal_icon(key: &str) -> &'static str {
    match key {
"#,
    );
    let mut items: Vec<_> = icons.navigation.iter().collect();
    items.sort_by(|a, b| a.0.cmp(b.0));
    for (key, icon) in items {
        output.push_str(&format!("        \"{}\" => \"{}\",\n", key, icon.terminal));
    }
    output.push_str("        _ => \"·\",\n    }\n}\n\n");

    output.push_str(
        r#"/// Get terminal icon for a state.
pub fn state_terminal_icon(key: &str) -> &'static str {
    match key {
"#,
    );
    let mut items: Vec<_> = icons.states.iter().collect();
    items.sort_by(|a, b| a.0.cmp(b.0));
    for (key, icon) in items {
        output.push_str(&format!("        \"{}\" => \"{}\",\n", key, icon.terminal));
    }
    output.push_str("        _ => \"·\",\n    }\n}\n\n");

    output.push_str(
        r#"/// Get terminal icon for a mode.
pub fn mode_terminal_icon(key: &str) -> &'static str {
    match key {
"#,
    );
    let mut items: Vec<_> = icons.modes.iter().collect();
    items.sort_by(|a, b| a.0.cmp(b.0));
    for (key, icon) in items {
        output.push_str(&format!("        \"{}\" => \"{}\",\n", key, icon.terminal));
    }
    output.push_str("        _ => \"·\",\n    }\n}\n");

    Ok(output)
}

