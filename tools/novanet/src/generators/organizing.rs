#[cfg(test)]
mod tests {
    use super::*;
    use crate::generators::Generator;
    use crate::parsers::organizing::{ArcFamilyDef, LayerDef, RealmDef, TraitDef};

    #[test]
    fn generate_small_cypher() {
        let doc = OrganizingDoc {
            version: "9.0.0".to_string(),
            realms: vec![RealmDef {
                key: "test".to_string(),
                display_name: "Test".to_string(),
                emoji: "🧪".to_string(),
                color: "#000000".to_string(),
                llm_context: "Test realm.".to_string(),
                layers: vec![LayerDef {
                    key: "base".to_string(),
                    display_name: "Base".to_string(),
                    emoji: "📋".to_string(),
                    color: "#111111".to_string(),
                    llm_context: "Base layer.".to_string(),
                }],
            }],
            traits: vec![TraitDef {
                key: "fixed".to_string(),
                display_name: "Fixed".to_string(),
                color: "#222222".to_string(),
                llm_context: "Fixed trait.".to_string(),
            }],
            arc_families: vec![ArcFamilyDef {
                key: "owns".to_string(),
                display_name: "Owns".to_string(),
                color: "#333333".to_string(),
                arrow_style: "-->".to_string(),
                llm_context: "Ownership.".to_string(),
            }],
        };

        let cypher = generate_cypher(&doc).unwrap();

        // Realm
        assert!(cypher.contains("MERGE (r_test:Meta:Realm {key: 'test'})"));
        assert!(cypher.contains("r_test.display_name = 'Test'"));
        assert!(cypher.contains("r_test.color = '#000000'"));

        // Layer
        assert!(cypher.contains("MERGE (l_base:Meta:Layer {key: 'base'})"));
        assert!(cypher.contains("l_base.emoji = '📋'"));

        // HAS_LAYER
        assert!(cypher.contains("MATCH (r:Realm {key: 'test'}), (l:Layer {key: 'base'})"));
        assert!(cypher.contains("MERGE (r)-[:HAS_LAYER]->(l)"));

        // Trait
        assert!(cypher.contains("MERGE (t_fixed:Meta:Trait {key: 'fixed'})"));
        assert!(cypher.contains("t_fixed.color = '#222222'"));

        // ArcFamily
        assert!(cypher.contains("MERGE (ef_owns:Meta:ArcFamily {key: 'owns'})"));
        assert!(cypher.contains("ef_owns.arrow_style = '-->'"));

        // :Meta double-label in all MERGE statements
        assert!(cypher.contains(":Meta:Realm"));
        assert!(cypher.contains(":Meta:Layer"));
        assert!(cypher.contains(":Meta:Trait"));
        assert!(cypher.contains(":Meta:ArcFamily"));

        // Timestamps
        assert!(cypher.contains("created_at = datetime()"));
        assert!(cypher.contains("updated_at = datetime()"));

        // Header
        assert!(cypher.contains("v9.0.0"));
        assert!(cypher.contains("AUTO-GENERATED"));
        assert!(cypher.contains("1 Realms, 1 Layers, 1 Traits, 1 ArcFamilies"));
    }

    #[test]
    fn generate_multiline_llm_context() {
        let doc = OrganizingDoc {
            version: "9.0.0".to_string(),
            realms: vec![RealmDef {
                key: "r".to_string(),
                display_name: "R".to_string(),
                emoji: "X".to_string(),
                color: "#fff".to_string(),
                llm_context: "Line one.\n  Line two.\n  Line three.\n".to_string(),
                layers: vec![LayerDef {
                    key: "l".to_string(),
                    display_name: "L".to_string(),
                    emoji: "Y".to_string(),
                    color: "#000".to_string(),
                    llm_context: "Layer.".to_string(),
                }],
            }],
            traits: vec![TraitDef {
                key: "t".to_string(),
                display_name: "T".to_string(),
                color: "#aaa".to_string(),
                llm_context: "Trait.".to_string(),
            }],
            arc_families: vec![ArcFamilyDef {
                key: "e".to_string(),
                display_name: "E".to_string(),
                color: "#bbb".to_string(),
                arrow_style: "-->".to_string(),
                llm_context: "Edge.".to_string(),
            }],
        };

        let cypher = generate_cypher(&doc).unwrap();

        // Multiline collapsed to single line
        assert!(cypher.contains("r_r.llm_context = 'Line one. Line two. Line three.'"));
    }

    #[test]
    fn generate_organizing_integration() {
        let root = std::path::Path::new(env!("CARGO_MANIFEST_DIR"))
            .parent()
            .and_then(|p| p.parent());

        let Some(root) = root else { return };
        if !root.join("pnpm-workspace.yaml").exists() {
            return;
        }

        let generator = OrganizingGenerator;
        let cypher = generator.generate(root).expect("should generate cypher");

        // Count MERGE statements per meta-node type
        let count_merges = |label: &str| {
            cypher
                .lines()
                .filter(|l: &&str| l.contains("MERGE") && l.contains(&format!(":Meta:{label}")))
                .count()
        };

        assert_eq!(count_merges("Realm"), 3, "expected 3 Realm nodes");
        assert_eq!(count_merges("Layer"), 9, "expected 9 Layer nodes");
        assert_eq!(count_merges("Trait"), 5, "expected 5 Trait nodes");
        assert_eq!(count_merges("ArcFamily"), 5, "expected 5 ArcFamily nodes");

        // HAS_LAYER relationships
        let has_layer_count = cypher
            .lines()
            .filter(|l: &&str| l.contains("[:HAS_LAYER]"))
            .count();
        assert_eq!(has_layer_count, 9, "expected 9 HAS_LAYER relationships");

        // Spot checks — specific nodes exist
        assert!(cypher.contains("r_global:Meta:Realm {key: 'global'}"));
        assert!(cypher.contains("r_project:Meta:Realm {key: 'project'}"));
        assert!(cypher.contains("r_shared:Meta:Realm {key: 'shared'}"));
        assert!(cypher.contains("l_knowledge:Meta:Layer {key: 'knowledge'}"));
        assert!(cypher.contains("l_foundation:Meta:Layer {key: 'foundation'}"));
        assert!(cypher.contains("l_seo:Meta:Layer {key: 'seo'}"));
        assert!(cypher.contains("t_localized:Meta:Trait {key: 'localized'}"));
        assert!(cypher.contains("t_invariant:Meta:Trait {key: 'invariant'}"));
        assert!(cypher.contains("ef_semantic:Meta:ArcFamily {key: 'semantic'}"));
        assert!(cypher.contains("ef_mining:Meta:ArcFamily {key: 'mining'}"));

        // Spot check — arrow_style preserved
        assert!(cypher.contains("ef_semantic.arrow_style = '.->'"));
        assert!(cypher.contains("ef_generation.arrow_style = '==>'"));
        assert!(cypher.contains("ef_mining.arrow_style = '--o'"));

        // Header mentions v9.0.0
        assert!(cypher.contains("v9.0.0"));

        // HAS_LAYER wiring — specific pairs
        assert!(cypher.contains("(r:Realm {key: 'global'}), (l:Layer {key: 'config'})"));
        assert!(cypher.contains("(r:Realm {key: 'project'}), (l:Layer {key: 'output'})"));
        assert!(cypher.contains("(r:Realm {key: 'shared'}), (l:Layer {key: 'geo'})"));
    }
}
