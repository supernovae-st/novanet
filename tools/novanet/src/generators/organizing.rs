/// Generates Cypher for the 4 taxonomy dimensions from `taxonomy.yaml`.
pub struct OrganizingGenerator;

impl super::Generator for OrganizingGenerator {
    fn name(&self) -> &'static str {
        "taxonomy"
    }

    fn generate(&self, root: &Path) -> crate::Result<String> {
        // v0.12.5: Load from individual YAML files instead of taxonomy.yaml
        let doc = crate::parsers::taxonomy::load_taxonomy_from_files(root)?;
        generate_cypher(&doc)
    }
}

/// Build the full Cypher output from a parsed taxonomy document.
fn generate_cypher(doc: &TaxonomyDoc) -> crate::Result<String> {
    let total_layers: usize = doc.node_realms.iter().map(|r| r.layers.len()).sum();
    let mut out = String::with_capacity(8192);

    // ── Header ───────────────────────────────────────────────────────────
    writeln!(out, "// Taxonomy Seed v{}", doc.version).unwrap();
    writeln!(out, "// AUTO-GENERATED by novanet — do not edit manually").unwrap();
    writeln!(out, "// Run: novanet schema generate").unwrap();
    writeln!(out, "//").unwrap();
    writeln!(
        out,
        "// Creates: {} Realms, {} Layers, {} Traits, {} ArcFamilies",
        doc.node_realms.len(),
        total_layers,
        doc.node_traits.len(),
        doc.arc_families.len()
    )
    .unwrap();
    writeln!(out, "// Includes visual encoding properties").unwrap();
    writeln!(out, "// All schema-nodes have :Schema double-label").unwrap();
    writeln!(out, "// Uses MERGE for idempotent execution").unwrap();

    // ── Realms ───────────────────────────────────────────────────────────
    writeln!(out).unwrap();
    write_section_header_counted(&mut out, "REALMS", doc.node_realms.len());

    for realm in &doc.node_realms {
        writeln!(out).unwrap();
        let var = format!("r_{}", realm.key);
        let llm = cypher_str(&realm.llm_context);
        write_merge_meta(
            &mut out,
            &var,
            "Realm",
            &realm.key,
            &[
                ("display_name", &realm.display_name),
                ("emoji", realm.emoji()),
                ("color", &realm.color),
                ("llm_context", &llm),
            ],
        );
    }

    // ── Layers + HAS_LAYER rels ──────────────────────────────────────────
    writeln!(out).unwrap();
    write_section_header_counted(&mut out, "LAYERS", total_layers);

    for realm in &doc.node_realms {
        writeln!(out).unwrap();
        writeln!(out, "// {} layers", realm.display_name).unwrap();

        for layer in &realm.layers {
            // Replace hyphens with underscores for valid Cypher variable names
            let var = format!("l_{}", layer.key.replace('-', "_"));
            let llm = cypher_str(&layer.llm_context);
            write_merge_meta(
                &mut out,
                &var,
                "Layer",
                &layer.key,
                &[
                    ("display_name", &layer.display_name),
                    ("emoji", layer.emoji()),
                    ("color", &layer.color),
                    ("llm_context", &llm),
                ],
            );
            writeln!(out).unwrap();
            writeln!(
                out,
                "MATCH (r:Realm {{key: '{}'}}), (l:Layer {{key: '{}'}})",
                realm.key, layer.key
            )
            .unwrap();
            writeln!(out, "MERGE (r)-[:HAS_LAYER]->(l);").unwrap();
        }
    }

    // ── Traits (with visual encoding) ────────────────────────────────────
    writeln!(out).unwrap();
    write_section_header_counted(&mut out, "TRAITS", doc.node_traits.len());

    for trait_def in &doc.node_traits {
        writeln!(out).unwrap();
        let var = format!("t_{}", trait_def.key);
        let llm = cypher_str(&trait_def.llm_context);

        // Start MERGE
        writeln!(
            out,
            "MERGE ({var}:Schema:Trait {{key: '{}'}})",
            trait_def.key
        )
        .unwrap();
        writeln!(out, "ON CREATE SET").unwrap();
        writeln!(out, "  {var}.created_at = datetime(),").unwrap();
        writeln!(out, "  {var}.updated_at = datetime()").unwrap();
        writeln!(out, "ON MATCH SET").unwrap();
        writeln!(out, "  {var}.updated_at = datetime()").unwrap();
        writeln!(out, "SET").unwrap();
        writeln!(out, "  {var}.display_name = '{}',", trait_def.display_name).unwrap();
        writeln!(out, "  {var}.color = '{}',", trait_def.color).unwrap();

        // Visual encoding properties
        if let Some(ref style) = trait_def.border_style {
            writeln!(out, "  {var}.border_style = '{style}',").unwrap();
        }
        if let Some(width) = trait_def.border_width {
            writeln!(out, "  {var}.border_width = {width},").unwrap();
        }
        if let Some(ref unicode) = trait_def.unicode_border {
            writeln!(out, "  {var}.unicode_border = '{unicode}',").unwrap();
        }

        writeln!(out, "  {var}.llm_context = '{llm}';").unwrap();
    }

    // ── Arc Families (with visual encoding) ──────────────────────────────
    writeln!(out).unwrap();
    write_section_header_counted(&mut out, "ARC FAMILIES", doc.arc_families.len());

    for af in &doc.arc_families {
        writeln!(out).unwrap();
        let var = format!("af_{}", af.key);
        let llm = cypher_str(&af.llm_context);

        // Start MERGE
        writeln!(out, "MERGE ({var}:Schema:ArcFamily {{key: '{}'}})", af.key).unwrap();
        writeln!(out, "ON CREATE SET").unwrap();
        writeln!(out, "  {var}.created_at = datetime(),").unwrap();
        writeln!(out, "  {var}.updated_at = datetime()").unwrap();
        writeln!(out, "ON MATCH SET").unwrap();
        writeln!(out, "  {var}.updated_at = datetime()").unwrap();
        writeln!(out, "SET").unwrap();
        writeln!(out, "  {var}.display_name = '{}',", af.display_name).unwrap();
        writeln!(out, "  {var}.color = '{}',", af.color).unwrap();
        writeln!(out, "  {var}.arrow_style = '{}',", af.arrow_style).unwrap();

        // Visual encoding properties
        if let Some(ref style) = af.stroke_style {
            writeln!(out, "  {var}.stroke_style = '{style}',").unwrap();
        }
        if let Some(width) = af.stroke_width {
            writeln!(out, "  {var}.stroke_width = {width},").unwrap();
        }

        // Retrieval default traversal mode
        if let Some(ref traversal) = af.default_traversal {
            writeln!(out, "  {var}.default_traversal = '{traversal}',").unwrap();
        }

        writeln!(out, "  {var}.llm_context = '{llm}';").unwrap();
    }

    // ── Arc Scopes  ────────────────────────────────────────────────
    if !doc.arc_scopes.is_empty() {
        writeln!(out).unwrap();
        write_section_header_counted(&mut out, "ARC SCOPES", doc.arc_scopes.len());

        for scope in &doc.arc_scopes {
            writeln!(out).unwrap();
            let var = format!("as_{}", scope.key);
            write_merge_meta(
                &mut out,
                &var,
                "ArcScope",
                &scope.key,
                &[
                    ("display_name", &scope.display_name),
                    ("description", &scope.description),
                ],
            );
        }
    }

    // ── Arc Cardinalities  ─────────────────────────────────────────
    if !doc.arc_cardinalities.is_empty() {
        writeln!(out).unwrap();
        write_section_header_counted(&mut out, "ARC CARDINALITIES", doc.arc_cardinalities.len());

        for card in &doc.arc_cardinalities {
            writeln!(out).unwrap();
            let var = format!("ac_{}", card.key);
            write_merge_meta(
                &mut out,
                &var,
                "ArcCardinality",
                &card.key,
                &[
                    ("display_name", &card.display_name),
                    ("description", &card.description),
                ],
            );
        }
    }

    writeln!(out).unwrap();

    Ok(out)
}

