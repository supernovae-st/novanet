    #[test]
    fn generate_taxonomy_integration() {
        let root = std::path::Path::new(env!("CARGO_MANIFEST_DIR"))
            .parent()
            .and_then(|p| p.parent());

        let Some(root) = root else { return };
        if !root.join("pnpm-workspace.yaml").exists() {
            return;
        }

        let generator = OrganizingGenerator;
        let cypher = generator.generate(root).expect("should generate cypher");

        // Count MERGE statements per meta-node type
        let count_merges = |label: &str| {
            cypher
                .lines()
                .filter(|l: &&str| l.contains("MERGE") && l.contains(&format!(":Schema:{label}")))
                .count()
        };

        assert_eq!(count_merges("Realm"), 2, "expected 2 Realm nodes"); // v11.4: shared + org
        assert_eq!(count_merges("Layer"), 10, "expected 10 Layer nodes"); // v11.4: 4 shared + 6 org
        assert_eq!(count_merges("Trait"), 5, "expected 5 Trait nodes"); // v11.2: split derived → generated + aggregated
        assert_eq!(count_merges("ArcFamily"), 6, "expected 6 ArcFamily nodes"); // v0.13.1: added schema family
        assert_eq!(count_merges("ArcScope"), 2, "expected 2 ArcScope nodes");
        assert_eq!(
            count_merges("ArcCardinality"),
            5,
            "expected 5 ArcCardinality nodes"
        );

        // HAS_LAYER relationships
        let has_layer_count = cypher
            .lines()
            .filter(|l: &&str| l.contains("[:HAS_LAYER]"))
            .count();
        assert_eq!(has_layer_count, 10, "expected 10 HAS_LAYER relationships"); // v11.4: 4 shared + 6 org

        // Spot checks — specific nodes exist (v11.4: 2 realms, 10 layers)
        assert!(cypher.contains("r_shared:Schema:Realm {key: 'shared'}"));
        assert!(cypher.contains("r_org:Schema:Realm {key: 'org'}")); // v11.2: org realm
        assert!(cypher.contains("l_config:Schema:Layer {key: 'config'}")); // v11.4: shared.config
        assert!(cypher.contains("l_locale:Schema:Layer {key: 'locale'}")); // v11.3: shared.locale
        assert!(cypher.contains("l_geography:Schema:Layer {key: 'geography'}")); // v11.3: shared.geography
        assert!(cypher.contains("l_knowledge:Schema:Layer {key: 'knowledge'}")); // v11.3: shared.knowledge
        assert!(cypher.contains("l_foundation:Schema:Layer {key: 'foundation'}"));
        assert!(cypher.contains("l_semantic:Schema:Layer {key: 'semantic'}")); // v10.6: tenant.semantic
        // v11.4: seo/geo layers removed (nodes moved to shared/knowledge)
        assert!(cypher.contains("t_authored:Schema:Trait {key: 'authored'}"));
        assert!(cypher.contains("t_defined:Schema:Trait {key: 'defined'}"));
        assert!(cypher.contains("af_semantic:Schema:ArcFamily {key: 'semantic'}"));
        assert!(cypher.contains("af_mining:Schema:ArcFamily {key: 'mining'}"));

        // Spot check — arrow_style preserved
        assert!(cypher.contains("af_semantic.arrow_style = '.->'"));
        assert!(cypher.contains("af_generation.arrow_style = '==>'"));
        assert!(cypher.contains("af_mining.arrow_style = '--o'"));

        // Visual encoding — border styles on traits
        assert!(cypher.contains("t_defined.border_style = 'solid'"));
        assert!(cypher.contains("t_authored.border_style = 'dashed'"));

        // Visual encoding — stroke styles on arc families
        assert!(cypher.contains("af_ownership.stroke_style = 'solid'"));
        assert!(cypher.contains("af_localization.stroke_style = 'dashed'"));

        // Arc scopes and cardinalities
        assert!(cypher.contains("as_intra_realm:Schema:ArcScope {key: 'intra_realm'}"));
        assert!(cypher.contains("ac_one_to_many:Schema:ArcCardinality {key: 'one_to_many'}"));

        // Header mentions version
        assert!(cypher.contains("v0.13.0"));

        // HAS_LAYER wiring — specific pairs (v11.4: shared has config, locale, geography, knowledge)
        assert!(cypher.contains("(r:Realm {key: 'shared'}), (l:Layer {key: 'config'})"));
        assert!(cypher.contains("(r:Realm {key: 'shared'}), (l:Layer {key: 'locale'})"));
        assert!(cypher.contains("(r:Realm {key: 'shared'}), (l:Layer {key: 'geography'})"));
        assert!(cypher.contains("(r:Realm {key: 'shared'}), (l:Layer {key: 'knowledge'})"));
        assert!(cypher.contains("(r:Realm {key: 'org'}), (l:Layer {key: 'foundation'})"));
        assert!(cypher.contains("(r:Realm {key: 'org'}), (l:Layer {key: 'output'})"));
        // v11.4: seo/geo removed from org
    }

