//! Generate v9 Cypher for Realm, Layer, Trait, ArcFamily meta-nodes.
//!
//! Reads `organizing-principles.yaml` and produces idempotent MERGE statements
//! for the 4 taxonomy dimensions. All meta-nodes receive `:Meta` double-label.
//!
//! Output target: `packages/db/seed/00.5-organizing-principles.cypher`

use super::cypher_utils::{cypher_str, write_merge_meta};
use crate::parsers::organizing::OrganizingDoc;
use std::fmt::Write;
use std::path::Path;

// ─────────────────────────────────────────────────────────────────────────────
// Generator
// ─────────────────────────────────────────────────────────────────────────────

/// Generates Cypher for the 4 taxonomy dimensions from `organizing-principles.yaml`.
pub struct OrganizingGenerator;

impl super::Generator for OrganizingGenerator {
    fn name(&self) -> &'static str {
        "organizing-principles"
    }

    fn generate(&self, root: &Path) -> crate::Result<String> {
        let doc = crate::parsers::organizing::load_organizing(root)?;
        generate_cypher(&doc)
    }
}

/// Build the full Cypher output from a parsed organizing document.
fn generate_cypher(doc: &OrganizingDoc) -> crate::Result<String> {
    let total_layers: usize = doc.realms.iter().map(|r| r.layers.len()).sum();
    let mut out = String::with_capacity(8192);

    // ── Header ───────────────────────────────────────────────────────────
    writeln!(out, "// Organizing Principles Seed v{}", doc.version).unwrap();
    writeln!(out, "// AUTO-GENERATED by novanet — do not edit manually").unwrap();
    writeln!(out, "// Run: novanet schema generate").unwrap();
    writeln!(out, "//").unwrap();
    writeln!(
        out,
        "// Creates: {} Realms, {} Layers, {} Traits, {} ArcFamilies",
        doc.realms.len(),
        total_layers,
        doc.traits.len(),
        doc.arc_families.len()
    )
    .unwrap();
    writeln!(out, "// All meta-nodes have :Meta double-label").unwrap();
    writeln!(out, "// Uses MERGE for idempotent execution").unwrap();

    // ── Realms ───────────────────────────────────────────────────────────
    writeln!(out).unwrap();
    write_section_header(&mut out, "REALMS", doc.realms.len());

    for realm in &doc.realms {
        writeln!(out).unwrap();
        let var = format!("r_{}", realm.key);
        let llm = cypher_str(&realm.llm_context);
        write_merge_meta(
            &mut out,
            &var,
            "Realm",
            &realm.key,
            &[
                ("display_name", &realm.display_name),
                ("emoji", &realm.emoji),
                ("color", &realm.color),
                ("llm_context", &llm),
            ],
        );
    }

    // ── Layers + HAS_LAYER rels ──────────────────────────────────────────
    writeln!(out).unwrap();
    write_section_header(&mut out, "LAYERS", total_layers);

    for realm in &doc.realms {
        writeln!(out).unwrap();
        writeln!(out, "// {} layers", realm.display_name).unwrap();

        for layer in &realm.layers {
            let var = format!("l_{}", layer.key);
            let llm = cypher_str(&layer.llm_context);
            write_merge_meta(
                &mut out,
                &var,
                "Layer",
                &layer.key,
                &[
                    ("display_name", &layer.display_name),
                    ("emoji", &layer.emoji),
                    ("color", &layer.color),
                    ("llm_context", &llm),
                ],
            );
            writeln!(out).unwrap();
            writeln!(
                out,
                "MATCH (r:Realm {{key: '{}'}}), (l:Layer {{key: '{}'}})",
                realm.key, layer.key
            )
            .unwrap();
            writeln!(out, "MERGE (r)-[:HAS_LAYER]->(l);").unwrap();
        }
    }

    // ── Traits ───────────────────────────────────────────────────────────
    writeln!(out).unwrap();
    write_section_header(&mut out, "TRAITS", doc.traits.len());

    for trait_def in &doc.traits {
        writeln!(out).unwrap();
        let var = format!("t_{}", trait_def.key);
        let llm = cypher_str(&trait_def.llm_context);
        write_merge_meta(
            &mut out,
            &var,
            "Trait",
            &trait_def.key,
            &[
                ("display_name", &trait_def.display_name),
                ("color", &trait_def.color),
                ("llm_context", &llm),
            ],
        );
    }

    // ── Edge Families ────────────────────────────────────────────────────
    writeln!(out).unwrap();
    write_section_header(&mut out, "ARC FAMILIES", doc.arc_families.len());

    for ef in &doc.arc_families {
        writeln!(out).unwrap();
        let var = format!("ef_{}", ef.key);
        let llm = cypher_str(&ef.llm_context);
        write_merge_meta(
            &mut out,
            &var,
            "ArcFamily",
            &ef.key,
            &[
                ("display_name", &ef.display_name),
                ("color", &ef.color),
                ("arrow_style", &ef.arrow_style),
                ("llm_context", &llm),
            ],
        );
    }

    writeln!(out).unwrap();

    Ok(out)
}

/// Write a visual section header comment.
fn write_section_header(out: &mut String, title: &str, count: usize) {
    let bar = "// ═══════════════════════════════════════════════════════════════════════════════";
    writeln!(out, "{bar}").unwrap();
    writeln!(out, "// {title} ({count})").unwrap();
    writeln!(out, "{bar}").unwrap();
}

