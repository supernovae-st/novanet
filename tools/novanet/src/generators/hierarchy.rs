//! Generate `hierarchy.ts` via MiniJinja template (taxonomy → TypeScript).
//!
//! Reads `taxonomy.yaml` and produces the realm → layer hierarchy tree
//! used by Studio for schema visualization and grouping.
//! Replaces the hand-written `hierarchy.ts` (v8: SCOPE_HIERARCHY → v9: REALM_HIERARCHY).
//!
//! Output target: `packages/core/src/graph/hierarchy.ts`

use crate::parsers::organizing;
use minijinja::{Environment, context};
use serde::Serialize;
use std::path::Path;

// ─────────────────────────────────────────────────────────────────────────────
// Template data structures
// ─────────────────────────────────────────────────────────────────────────────

/// A realm group for the template.
#[derive(Debug, Serialize)]
struct TemplateRealm {
    key: String,
    display_name: String,
    emoji: String,
    description: String,
    layer_count: usize,
    layers: Vec<TemplateLayer>,
}

/// A layer within a realm.
#[derive(Debug, Serialize)]
struct TemplateLayer {
    key: String,
    display_name: String,
    emoji: String,
    description: String,
}

// ─────────────────────────────────────────────────────────────────────────────
// Helpers
// ─────────────────────────────────────────────────────────────────────────────

/// Extract the first sentence from a multiline llm_context field.
/// Collapses whitespace, splits at ". ", and escapes for TS single-quoted strings.
fn first_sentence(text: &str) -> String {
    let collapsed: String = text.split_whitespace().collect::<Vec<_>>().join(" ");
    let sentence = if let Some(idx) = collapsed.find(". ") {
        collapsed[..=idx].to_string()
    } else if collapsed.ends_with('.') {
        collapsed
    } else {
        format!("{collapsed}.")
    };
    sentence.replace('\'', "\\'")
}

// ─────────────────────────────────────────────────────────────────────────────
// MiniJinja template
// ─────────────────────────────────────────────────────────────────────────────

const HIERARCHY_TEMPLATE: &str = r##"// packages/core/src/graph/hierarchy.ts
// REALM_HIERARCHY — v9 realm → layer tree with node type mappings
// AUTO-GENERATED by novanet v{{ version }} — DO NOT EDIT
// Run: novanet schema generate

import type { Realm } from '../types/nodes.js';
import type { Layer, RealmDefinition, LayerMeta } from './types.js';
import { getNodeTypesByRealmAndLayer } from './layers.js';

// =============================================================================
// REALM_HIERARCHY - Complete realm hierarchy definition
// =============================================================================

/**
 * Complete realm hierarchy definition.
 * This is the single source of truth for the ontology structure.
 *
 * {{ realm_count }} Realms ({{ total_layers }} layers):
{%- for realm in realms %}
 * - {{ realm.display_name }}: {{ realm.description }} ({{ realm.layer_count }} layer{{ "s" if realm.layer_count != 1 else "" }})
{%- endfor %}
 */
export const REALM_HIERARCHY: Record<Realm, RealmDefinition> = {
{%- for realm in realms %}
  // ═══════════════════════════════════════════════════════════════════════════
  // {{ realm.key | upper }} ({{ realm.layer_count }} layer{{ "s" if realm.layer_count != 1 else "" }})
  // ═══════════════════════════════════════════════════════════════════════════
  {{ realm.key }}: {
    realm: '{{ realm.key }}',
    label: '{{ realm.key | upper }}',
    icon: '{{ realm.emoji }}',
    description: '{{ realm.description }}',
    layers: {
{%- for layer in realm.layers %}
      '{{ layer.key }}': {
        label: '{{ layer.display_name }}',
        description: '{{ layer.description }}',
        icon: '{{ layer.emoji }}',
        nodeTypes: getNodeTypesByRealmAndLayer('{{ realm.key }}', '{{ layer.key }}'),
      },
{%- endfor %}
    } as Record<Layer, LayerMeta>,
  },
{%- endfor %}
};

// =============================================================================
// HELPER FUNCTIONS
// =============================================================================

/**
 * Get realm definition by realm key
 * @param realm - The realm to look up
 * @returns The realm definition
 */
export function getRealmDefinition(realm: Realm): RealmDefinition {
  return REALM_HIERARCHY[realm];
}

/**
 * Get layer metadata within a realm
 * @param realm - The realm to look up
 * @param layer - The layer to look up
 * @returns The layer metadata, or undefined if not found
 */
export function getLayerMeta(
  realm: Realm,
  layer: Layer
): LayerMeta | undefined {
  return REALM_HIERARCHY[realm]?.layers[layer];
}

/**
 * Get all layers for a realm
 * @param realm - The realm to look up
 * @returns Array of layer keys
 */
export function getLayersForRealm(realm: Realm): Layer[] {
  return Object.keys(REALM_HIERARCHY[realm].layers) as Layer[];
}
"##;

// ─────────────────────────────────────────────────────────────────────────────
// Generator
// ─────────────────────────────────────────────────────────────────────────────

pub struct HierarchyGenerator;

impl super::Generator for HierarchyGenerator {
    fn name(&self) -> &'static str {
        "hierarchy"
    }

    fn generate(&self, root: &Path) -> crate::Result<String> {
        let doc = organizing::load_organizing(root)?;
        render_hierarchy(&doc)
    }
}

fn render_hierarchy(doc: &organizing::OrganizingDoc) -> crate::Result<String> {
    let realms: Vec<TemplateRealm> = doc
        .realms
        .iter()
        .map(|r| TemplateRealm {
            key: r.key.clone(),
            display_name: r.display_name.clone(),
            emoji: r.emoji.clone(),
            description: first_sentence(&r.llm_context),
            layer_count: r.layers.len(),
            layers: r
                .layers
                .iter()
                .map(|l| TemplateLayer {
                    key: l.key.clone(),
                    display_name: l.display_name.clone(),
                    emoji: l.emoji.clone(),
                    description: first_sentence(&l.llm_context),
                })
                .collect(),
        })
        .collect();

    let total_layers: usize = realms.iter().map(|r| r.layer_count).sum();

    let mut env = Environment::new();
    env.add_template("hierarchy.ts", HIERARCHY_TEMPLATE)
        .map_err(|e| crate::NovaNetError::Generator {
            generator: "hierarchy".to_string(),
            detail: format!("template parse error: {e}"),
        })?;

    let tmpl = env
        .get_template("hierarchy.ts")
        .map_err(|e| crate::NovaNetError::Generator {
            generator: "hierarchy".to_string(),
            detail: format!("template not found: {e}"),
        })?;

    let output = tmpl
        .render(context! {
            version => &doc.version,
            realm_count => doc.realms.len(),
            total_layers => total_layers,
            realms => realms,
        })
        .map_err(|e| crate::NovaNetError::Generator {
            generator: "hierarchy".to_string(),
            detail: format!("template render error: {e}"),
        })?;

    Ok(output)
}

