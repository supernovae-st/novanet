    #[test]
    fn generate_hierarchy_integration() {
        let root = std::path::Path::new(env!("CARGO_MANIFEST_DIR"))
            .parent()
            .and_then(|p| p.parent());

        let Some(root) = root else { return };
        if !root.join("pnpm-workspace.yaml").exists() {
            return;
        }

        let generator = HierarchyGenerator;
        let output = generator
            .generate(root)
            .expect("should generate hierarchy.ts");

        // Header (version from taxonomy.yaml)
        assert!(output.contains("AUTO-GENERATED by novanet v0.13.0"));

        // v9 export
        assert!(output.contains("export const REALM_HIERARCHY: Record<Realm, RealmDefinition>"));

        // 2 realms (v11.3: shared + org)
        assert!(output.contains("shared: {"));
        assert!(output.contains("org: {")); // v11.2: org realm

        // v11.3: uses getNodeTypesByRealmAndLayer(realm, layer) calls
        // Check key realm/layer combinations (v11.4: SEO/GEO moved to shared/knowledge)
        assert!(output.contains("getNodeTypesByRealmAndLayer('shared', 'config')")); // v11.4: shared.config
        assert!(output.contains("getNodeTypesByRealmAndLayer('shared', 'locale')"));
        assert!(output.contains("getNodeTypesByRealmAndLayer('shared', 'geography')"));
        assert!(output.contains("getNodeTypesByRealmAndLayer('shared', 'knowledge')"));
        assert!(output.contains("getNodeTypesByRealmAndLayer('org', 'config')"));
        assert!(output.contains("getNodeTypesByRealmAndLayer('org', 'semantic')"));
        assert!(output.contains("getNodeTypesByRealmAndLayer('org', 'foundation')"));
        assert!(output.contains("getNodeTypesByRealmAndLayer('org', 'structure')"));
        assert!(output.contains("getNodeTypesByRealmAndLayer('org', 'instruction')"));
        assert!(output.contains("getNodeTypesByRealmAndLayer('org', 'output')"));
        // v11.4: seo/geo layers removed from org

        // Realm sections (v11.4: 4 shared + 6 org layers)
        assert!(output.contains("SHARED (4 layers)")); // config, locale, geography, knowledge
        assert!(output.contains("ORG (6 layers)")); // config, foundation, structure, semantic, instruction, output

        // Total
        assert!(output.contains("2 Realms (10 layers)")); // v11.4: 10 layers

        // Helper functions
        assert!(output.contains("export function getRealmDefinition"));
        assert!(output.contains("export function getLayerMeta"));
        assert!(output.contains("export function getLayersForRealm"));

        // Spot checks — display_names from YAML
        assert!(output.contains("label: 'Locale'"));
        assert!(output.contains("label: 'Config'")); // v11.4: shared/config layer
        // v11.4: SEO layer removed (nodes moved to shared/knowledge)

        // No v8 terms
        assert!(!output.contains("SCOPE_HIERARCHY"));
        assert!(!output.contains("getNodeTypesBySubcategory"));
    }

