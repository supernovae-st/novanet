#[cfg(test)]
mod tests {
    use super::*;

    fn test_root() -> Option<std::path::PathBuf> {
        let root = std::path::Path::new(env!("CARGO_MANIFEST_DIR"))
            .parent()
            .and_then(|p| p.parent());
        let root = root?;
        if !root.join("pnpm-workspace.yaml").exists() {
            return None;
        }
        Some(root.to_path_buf())
    }

    #[test]
    fn doc_generate_dry_run_all() {
        let Some(root) = test_root() else { return };

        let results = doc_generate(&root, None, true).expect("should generate all view docs");

        // At least 11 views (complete-graph is skipped, geo-pipeline removed in v10.1)
        assert!(
            results.len() >= 11,
            "expected at least 11 view docs, got {}",
            results.len()
        );

        // Verify no complete-graph in results
        assert!(
            !results.iter().any(|r| r.view_id == "complete-graph"),
            "complete-graph should not be generated by doc command"
        );

        for r in &results {
            assert!(
                r.bytes > 100,
                "view '{}': expected >100 bytes, got {}",
                r.view_id,
                r.bytes
            );
            assert!(
                r.output_path.ends_with(".md"),
                "view '{}': output should be .md",
                r.view_id
            );
            assert!(
                r.output_path.contains(&r.view_id),
                "view '{}': output path should contain view id",
                r.view_id
            );
        }
    }

    #[test]
    fn doc_generate_dry_run_single() {
        let Some(root) = test_root() else { return };

        let results = doc_generate(&root, Some("block-generation"), true)
            .expect("should generate single view");

        assert_eq!(results.len(), 1);
        assert_eq!(results[0].view_id, "block-generation");
        assert!(results[0].bytes > 100);
    }

    #[test]
    fn doc_generate_nonexistent_view() {
        let Some(root) = test_root() else { return };

        let result = doc_generate(&root, Some("nonexistent"), true);
        assert!(result.is_err());
    }

    #[test]
    fn doc_list_returns_views() {
        let Some(root) = test_root() else { return };

        let entries = doc_list(&root).expect("should list views");

        // 11 entries (complete-graph skipped, geo-pipeline removed in v10.1)
        assert!(
            entries.len() >= 11,
            "expected at least 11 list entries, got {}",
            entries.len()
        );

        // Verify no complete-graph
        assert!(
            !entries.iter().any(|e| e.id == "complete-graph"),
            "complete-graph should not appear in doc list"
        );

        // Every entry should have non-empty fields
        for e in &entries {
            assert!(!e.id.is_empty(), "empty id");
            assert!(!e.category.is_empty(), "empty category for {}", e.id);
            assert!(!e.description.is_empty(), "empty description for {}", e.id);
        }
    }
}
