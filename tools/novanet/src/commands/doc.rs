//! `novanet doc generate` and `novanet doc list` commands.
//!
//! - generate: Produces Mermaid-in-Markdown docs for individual views
//! - list: Shows available views with categories from the registry

use std::fs;
use std::path::Path;
use std::time::Instant;

// ─────────────────────────────────────────────────────────────────────────────
// Doc Generate
// ─────────────────────────────────────────────────────────────────────────────

/// Result of generating a single view document.
pub struct DocGenerateResult {
    pub view_id: String,
    pub view_name: String,
    pub output_path: String,
    pub bytes: usize,
    pub duration_ms: u128,
}

/// Generate Mermaid documentation for one or all views.
///
/// - If `view_id` is `Some`, generates only that view.
/// - If `view_id` is `None`, generates all views (skips `complete-graph`).
/// - If `dry_run` is `true`, generates output but does not write files.
///
/// Output: `packages/core/models/docs/<view-id>.md`
pub fn doc_generate(
    root: &Path,
    view_id: Option<&str>,
    dry_run: bool,
) -> crate::Result<Vec<DocGenerateResult>> {
    let views = match view_id {
        Some(id) => vec![crate::parsers::views::load_view(root, id)?],
        None => {
            let mut all = crate::parsers::views::load_all_views(root)?;
            // complete-graph is produced by `novanet schema generate`, skip it here
            all.retain(|v| v.id != "complete-graph");
            all
        }
    };

    let docs_dir = crate::config::docs_dir(root);
    let mut results = Vec::with_capacity(views.len());

    for view in &views {
        let start = Instant::now();
        let output = crate::generators::view_mermaid::generate_view(root, view)?;
        let duration_ms = start.elapsed().as_millis();
        let bytes = output.len();

        let rel_path = format!("packages/core/models/docs/{}.md", view.id);

        if !dry_run {
            fs::create_dir_all(&docs_dir).map_err(crate::NovaNetError::Io)?;
            let full_path = docs_dir.join(format!("{}.md", view.id));
            fs::write(&full_path, &output).map_err(crate::NovaNetError::Io)?;
        }

        results.push(DocGenerateResult {
            view_id: view.id.clone(),
            view_name: view.name.clone(),
            output_path: rel_path,
            bytes,
            duration_ms,
        });
    }

    Ok(results)
}

// ─────────────────────────────────────────────────────────────────────────────
// Doc List
// ─────────────────────────────────────────────────────────────────────────────

/// Entry for display in `novanet doc generate --list`.
pub struct DocListEntry {
    pub id: String,
    pub category: String,
    pub description: String,
}

/// List available views from the registry.
pub fn doc_list(root: &Path) -> crate::Result<Vec<DocListEntry>> {
    let registry = crate::parsers::views::load_registry(root)?;
    let entries = registry
        .views
        .into_iter()
        // Skip complete-graph (handled by schema generate)
        .filter(|v| v.id != "complete-graph")
        .map(|v| DocListEntry {
            id: v.id,
            category: v.category,
            description: v.description,
        })
        .collect();
    Ok(entries)
}

