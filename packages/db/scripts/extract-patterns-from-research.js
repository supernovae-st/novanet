#!/usr/bin/env node
/**
 * Extract Patterns from Perplexity Research JSON files
 *
 * Reads: germanic, asian, other, romance research files
 * Generates: 25.3-patterns.cypher
 */

const fs = require('fs');
const path = require('path');

const RESEARCH_FILES = [
  path.join(__dirname, '..', 'seed', 'data', 'germanic-locales-research.json'),
  path.join(__dirname, '..', '..', '..', 'tools', 'novanet', 'asian-locales-cultural-research.json'),
  path.join(__dirname, '..', '..', '..', 'tools', 'novanet', 'locale_cultural_data_other.json'),
  path.join(__dirname, '..', '..', '..', 'tools', 'novanet', 'romance-locales-cultural-data.json'),
];

const OUTPUT_FILE = path.join(__dirname, '..', 'seed', '25.3-patterns.cypher');

const stats = { patterns: 0, locales: 0 };

function escapeCypher(text) {
  if (!text) return '';
  return text
    .replace(/\\/g, '\\\\')
    .replace(/'/g, "\\'")
    .replace(/\n/g, ' ')
    .substring(0, 500);
}

function slugify(text) {
  return text
    .toLowerCase()
    .normalize('NFD')
    .replace(/[\u0300-\u036f]/g, '')
    .replace(/[^\w\s-]/g, '')
    .replace(/[\s_]+/g, '-')
    .replace(/-+/g, '-')
    .replace(/^-|-$/g, '')
    .substring(0, 50);
}

function processResearchFile(filePath) {
  const lines = [];

  if (!fs.existsSync(filePath)) {
    console.warn(`  ⚠️ File not found: ${filePath}`);
    return lines;
  }

  const content = JSON.parse(fs.readFileSync(filePath, 'utf-8'));

  // Handle both array and object formats for locales
  let localesArray = [];
  if (Array.isArray(content.locales)) {
    localesArray = content.locales;
  } else if (typeof content.locales === 'object' && content.locales !== null) {
    // Convert object format { "fr-FR": { ... } } to array format [{ locale: "fr-FR", ... }]
    localesArray = Object.entries(content.locales).map(([localeCode, data]) => ({
      locale: localeCode,
      ...data
    }));
  }

  console.log(`  📄 Processing ${path.basename(filePath)}: ${localesArray.length} locales`);

  for (const localeData of localesArray) {
    const locale = localeData.locale;
    if (!locale || !localeData.patterns || localeData.patterns.length === 0) continue;

    stats.locales++;

    lines.push(`// ----------------------------------------------------------------------------`);
    lines.push(`// ${locale} Patterns`);
    lines.push(`// ----------------------------------------------------------------------------`);
    lines.push('');

    for (const pattern of localeData.patterns) {
      const key = `pattern:${slugify(pattern.key || pattern.type + '-' + stats.patterns)}@${locale}`;
      const template = escapeCypher(pattern.template);
      const patternType = pattern.type || 'cta';
      const context = pattern.context || 'landing';
      const element = pattern.element || patternType;
      const tone = pattern.tone || 'professional';
      const constraints = pattern.constraints ? JSON.stringify(pattern.constraints) : '{}';
      const variables = pattern.variables ? JSON.stringify(pattern.variables) : '{}';

      lines.push(`MERGE (p:Pattern {key: '${key}'})`);
      lines.push(`SET p.display_name = '${escapeCypher(pattern.template || pattern.key)}',`);
      lines.push(`    p.locale = '${locale}',`);
      lines.push(`    p.template = '${template}',`);
      lines.push(`    p.type = '${escapeCypher(patternType)}',`);
      lines.push(`    p.context = '${escapeCypher(context)}',`);
      lines.push(`    p.element = '${escapeCypher(element)}',`);
      lines.push(`    p.tone = '${escapeCypher(tone)}',`);
      lines.push(`    p.constraints = '${escapeCypher(constraints)}',`);
      lines.push(`    p.variables = '${escapeCypher(variables)}',`);
      lines.push(`    p.llm_context = 'USE: ${escapeCypher(patternType)} pattern for ${locale}. TEMPLATE: ${escapeCypher(template)}. TONE: ${escapeCypher(tone)}.',`);
      lines.push(`    p.provenance = 'perplexity-research',`);
      lines.push(`    p.confidence = 0.85,`);
      lines.push(`    p.created_at = datetime(),`);
      lines.push(`    p.updated_at = datetime();`);
      lines.push('');

      // Link to PatternSet
      lines.push(`MATCH (ps:PatternSet {key: 'pattern-set:cta@${locale}'})`);
      lines.push(`MATCH (p:Pattern {key: '${key}'})`);
      lines.push(`MERGE (ps)-[:CONTAINS_PATTERN]->(p);`);
      lines.push('');

      stats.patterns++;
    }
  }

  return lines;
}

async function main() {
  console.log('🔄 Extracting Patterns from research files...\n');

  const header = [
    '// ============================================================================',
    '// Seed 25.3: Patterns from Perplexity Research',
    '// ============================================================================',
    '// Generated by: extract-patterns-from-research.js',
    `// Generated at: ${new Date().toISOString()}`,
    '// Source: Perplexity research on cultural patterns for QR Code AI',
    '// ============================================================================',
    '',
  ];

  const allLines = [...header];

  for (const file of RESEARCH_FILES) {
    const lines = processResearchFile(file);
    allLines.push(...lines);
  }

  fs.writeFileSync(OUTPUT_FILE, allLines.join('\n'));

  console.log(`
═══════════════════════════════════════════════════════════════════
📊 PATTERN EXTRACTION COMPLETE
═══════════════════════════════════════════════════════════════════
   Locales processed:  ${stats.locales}
   Patterns created:   ${stats.patterns}

📁 Output: packages/db/seed/25.3-patterns.cypher
`);
}

main().catch(console.error);
